// find maximum by math ----------------------->

function max(value1, value2 , value3){

    let maxIs = Math.max(value1, value2 , value3);

    return maxIs;
}

let maxIs = max(97,99,95);

console.log(maxIs);

// find minimum value by math ----------------------->

function min(value1, value2 , value3){

    let minIs = Math.min(value1, value2 , value3);

    return minIs;
}

let minIs = min(97,99,95);

console.log(minIs);